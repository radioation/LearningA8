#pragma rodata-name( pop )


uint8_t x = 0;
uint16_t y_offset = 0;

void draw_rock( ) {
    if( screen_memory[y_offset +x ] || screen_memory[y_offset+x+1] ) 
        return;
    screen_memory[y_offset+x] = 1;
    screen_memory[y_offset+x+1] = 2;
    screen_memory[y_offset+x+40] = 3;
    screen_memory[y_offset+x+41] = 4;
}

void draw_ship1( ) {
    if( screen_memory[y_offset +x ] || screen_memory[y_offset+x+1] ) 
        return;
    screen_memory[y_offset+x] = 0x86;
    screen_memory[y_offset+x+1] = 0x87;
}

void draw_ship2( ) {
    if( screen_memory[y_offset +x ] || screen_memory[y_offset+x+1] ) 
        return;
    screen_memory[y_offset+x] = 0x88;
    screen_memory[y_offset+x+1] = 0x89;
}

void draw_xship( ) {
    if( screen_memory[y_offset +x ] || screen_memory[y_offset+x+1] || screen_memory[y_offset+x-1] ) 
        return;
    screen_memory[y_offset+x] = 0x8B;
    screen_memory[y_offset+x-41] = 0x8C;
    screen_memory[y_offset+x-39] = 0x8D;
    screen_memory[y_offset+x+39] = 0x8E;
    screen_memory[y_offset+x+41] = 0x8F;
}



int main(void)
{
    uint8_t i,j,count,type;
    uint8_t scroll_pos = 0;
    uint16_t addr = (uint16_t)(charset);

    memset( text_memory, 0, 80 ); 
    memset( screen_memory, 0, 3520 ); // 4*22*40   (88 lines)


    //screen_memory[220] = 0x85; shot
    //screen_memory[310] = 0x8A; shot

    for( i=0; i < 85; i+=2 ) {
        count = rand() % 3 + 1;
            for( j=0; j < count; ++j ) {
                x = rand() % 38;
                type = rand() % 10;
                switch (type) {
                    case 0:
                        draw_rock();
                        break;
                    case 1:
                        draw_ship1();
                        break;
                    case 2:
                        draw_ship2();
                        break;
                    case 3:
                        draw_xship();
                        break;
                    case 5:
                        if( screen_memory[y_offset+x] == 0 ) screen_memory[y_offset+x] = 0x85;
                        break;
                    case 6:
                        if( screen_memory[y_offset+x] == 0 ) screen_memory[y_offset+x] = 0x8A;
                        break;
                    default:
                        break;
                };

            } 
        y_offset += 80;     
    } 


    _setcolor(0, 0, 12);
    _setcolor(1, 0, 8);
    _setcolor(2, 0, 4);
    _setcolor(3, 2, 10);

    OS.chbas = addr >> 8;
    init_dlist();


    while(1) {
    
    }

}


